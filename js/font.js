function buildWordSet(wordObj) {
    const text = wordObj.word.toUpperCase();
    const len = text.length;

    const maxPsByW = Math.floor(GRID_W / (6 * len - 1));
    const maxPsByH = Math.floor(WORD_ZONE_H / 7);
    let ps = Math.min(maxPsByW, maxPsByH);
    ps = Math.max(1, Math.min(ps, 8));

    const fontPxW = ps * (6 * len - 1);
    const fontPxH = ps * 7;

    const startX = OX + Math.floor((GRID_W - fontPxW) / 2);
    const startY = OY + Math.floor((WORD_ZONE_H - fontPxH) / 2);

    const set = new Set();

    for (let ci = 0; ci < len; ci++) {
        const bm = FONT5[text[ci]];
        if (!bm) continue;
        const charStartX = startX + ci * (5 * ps + ps);

        for (let py = 0; py < 7; py++) {
            for (let px = 0; px < 5; px++) {
                if (!bm[py][px]) continue;
                const bx = charStartX + px * ps;
                const by = startY + py * ps;

                for (let dy = 0; dy < ps; dy++) {
                    for (let dx = 0; dx < ps; dx++) {
                        const rx = bx + dx - OX;
                        const ry = by + dy - OY;
                        if (rx < 0 || ry < 0) continue;
                        if (rx % CELL < SZ && ry % CELL < SZ) {
                            const col = Math.floor(rx / CELL);
                            const row = Math.floor(ry / CELL);
                            if (row >= 0 && row < ROWS && col >= 0 && col < COLS)
                                set.add(row + ',' + col);
                        }
                    }
                }
            }
        }
    }
    return set;
}